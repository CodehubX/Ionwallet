package com.fast.lib.widget;

import android.content.Context;
import android.util.AttributeSet;
import android.webkit.WebView;

/**
 * Created by siberiawolf on 16/1/4.
 */
public class LibraryWebView extends WebView {
    public LibraryWebView(Context context) {
        super(context);
    }

    public LibraryWebView(Context context, AttributeSet attrs) {
        super(context, attrs);
    }

    public LibraryWebView(Context context, AttributeSet attrs, int defStyleAttr) {
        super(context, attrs, defStyleAttr);
    }

    public LibraryWebView(Context context, AttributeSet attrs, int defStyleAttr, int defStyleRes) {
        super(context, attrs, defStyleAttr, defStyleRes);
    }

    public LibraryWebView(Context context, AttributeSet attrs, int defStyleAttr, boolean privateBrowsing) {
        super(context, attrs, defStyleAttr, privateBrowsing);
    }
}
